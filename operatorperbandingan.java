public class operatorperbandingan{
    public static void main(String[] args){
        int a = 7;
        int b = 5;
        String x = "merah";
        String y = "biru";
        System.out.println("a < b ->"+(a < b));
        System.out.println("a > b -> "+(a > b));
        System.out.println("a >= b -> "+(a >= b));
        System.out.println("a <= b -> "+(a <= b));
        System.out.println("a == b -> "+(a == b));
        System.out.println("a != b -> "+(a != b));
        System.out.println("merah == biru ->"+(x == y));
        System.out.println("merah != biru ->"+(x != y));
    }
}