public class operator{
    public static void main(String[] args){
        int a = 7;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);//modulus
        
        System.out.println(++a);
        System.out.println(a);
        b++;
        System.out.println(b);
        
        a--;
        System.out.println(a);
        b--;
        System.out.println(b);
    }
}